  // Round trip Skunk EWKT parse => PostGIS EWKT encode => Skunk EWKB decode
  private def roundtripEWKT(ewkt: String) = {
    sessionTest(ewkt) { s =>
      EWKT.parse(ewkt).fold(
        err => fail(s"EWKT parse failed for '$ewkt': $err"),
        parsedGeometry => {
          val roundtripGeom =
            s.prepare(sql"select '#$ewkt'::geometry".query(geometry))
              .flatMap(ps => ps.unique(Void))

          roundtripGeom.map { returnedGeometry =>
            assertEquals(returnedGeometry, parsedGeometry)
          }
        }
      )
    }
  }
